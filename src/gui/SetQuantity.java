    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String qty = jTextField1.getText();
        String qtyUnit = jComboBox1.getSelectedItem().toString();
        try {

            if (qty.isBlank()) {
                JOptionPane.showMessageDialog(this, "Enter The Qty.", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (qtyUnit.equals("Select")) {
                JOptionPane.showMessageDialog(this, "Enter Quantity Unit.", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                //Stock multiplication
                int selectedRowOfTable1 = this.cs.jTable1.getSelectedRow();
                ResultSet stockMultirs = MySQL.search("SELECT * FROM `qty_units` WHERE `name` = '" + this.cs.jTable1.getValueAt(selectedRowOfTable1, 4).toString() + "';");
                stockMultirs.next();
                double stmul = Double.parseDouble(stockMultirs.getString("multiplication"));
                //Stock multiplication

                //new multiplication
                ResultSet newMultirs = MySQL.search("SELECT * FROM `qty_units` WHERE `name` = '" + qtyUnit + "';");
                newMultirs.next();
                double stmulnew = Double.parseDouble(newMultirs.getString("multiplication"));
                //new multiplication

                double enteredQty = Double.parseDouble(qty);
                double stockQty = Double.parseDouble(this.cs.jTable1.getValueAt(selectedRowOfTable1, 3).toString());
                System.out.println(enteredQty * stmul * stmulnew);
                if ((enteredQty * stmul * stmulnew) > stockQty) {
                    JOptionPane.showMessageDialog(this, "Invalid Quantity. Enter Quantity below " + String.valueOf(stockQty) + " " + this.cs.jTable1.getValueAt(selectedRowOfTable1, 4).toString(), "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    boolean isThere = false;
                    int r = -1;
                    for (int i = 0; i < this.cs.jTable2.getRowCount(); i++) {
                        if (this.cs.jTable2.getValueAt(i, 0).toString().equals((this.cs.jTable1.getValueAt(selectedRowOfTable1, 0).toString()))) {
                            isThere = true;
                            r = i;
                            break;
                        }
                    }

                    if (isThere) {
                        int con = JOptionPane.showConfirmDialog(this, "This Product Already Added. Do you want to update the quantity?", "Warning", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
                        if (con == JOptionPane.YES_OPTION) {
                            ResultSet qtyUnitMulOld = MySQL.search("SELECT * FROM `qty_units` WHERE `name` = '" + this.cs.jTable2.getValueAt(r, 3).toString() + "';");
                            qtyUnitMulOld.next();
                            double mulOld = Double.parseDouble(qtyUnitMulOld.getString("multiplication"));
                            double oldQty = Double.parseDouble(this.cs.jTable2.getValueAt(r, 2).toString());
                            double newQty = oldQty + (enteredQty * stmulnew * mulOld);
                            if ((newQty * stmul * mulOld) > stockQty) {
                                JOptionPane.showMessageDialog(this, "Invalid Quantity. Enter Quantity below " + String.valueOf(stockQty - oldQty) + " " + this.cs.jTable1.getValueAt(selectedRowOfTable1, 4).toString(), "Warning", JOptionPane.WARNING_MESSAGE);
                            } else {
                                this.cs.jTable2.setValueAt(String.valueOf(newQty), r, 2);
                                this.cs.jTable2.setValueAt(String.valueOf(newQty * Double.parseDouble(this.cs.jTable1.getValueAt(selectedRowOfTable1, 2).toString())), r, 4);
                                this.cs.calculateTotal();
                                this.dispose();
                            }
                        }
                    } else {
                        DefaultTableModel dtm = (DefaultTableModel) this.cs.jTable2.getModel();
                        Vector v = new Vector();
                        v.add(this.cs.jTable1.getValueAt(selectedRowOfTable1, 0).toString());
                        v.add(this.cs.jTable1.getValueAt(selectedRowOfTable1, 1).toString());
                        v.add(String.valueOf(enteredQty));
                        v.add(jComboBox1.getSelectedItem().toString());
                        v.add(String.valueOf(enteredQty * stmul * stmulnew * Double.parseDouble(this.cs.jTable1.getValueAt(selectedRowOfTable1, 2).toString())));
                        dtm.addRow(v);
                        this.cs.calculateTotal();
                        this.dispose();
                    }

                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

