    public void loadQtyUnits(String pid) {
        try {
            ResultSet rs = MySQL.search("(SELECT * FROM `qty_units` INNER JOIN `qty_type` ON `qty_type`.`id` = `qty_units`.`qty_type_id` LEFT JOIN `product` ON `product`.`qty_type_id` = `qty_type`.`id` WHERE `product`.`id` = '" + pid + "') UNION (SELECT * FROM `qty_units` INNER JOIN `qty_type` ON `qty_type`.`id` = `qty_units`.`qty_type_id` RIGHT JOIN `product` ON `product`.`qty_type_id` = `qty_type`.`id` WHERE `product`.`id` = '" + pid + "');");
            Vector v = new Vector();
            v.add("Select");
            while (rs.next()) {
                v.add(rs.getString("name"));
            }
            DefaultComboBoxModel dcm = new DefaultComboBoxModel(v);
            jComboBox1.setModel(dcm);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

